public class swapping {
    public static void main(String[] args){

        
        String contOne = "Water";
        String contTwo = "Tang-Mango";
        String extraCont; // temporary value

        extraCont = contOne;
        contOne = contTwo;
        contTwo = extraCont;
        extraCont = "empty :D";



        System.out.println(contOne);
        System.out.println(contTwo);
        System.out.println(extraCont);






    }
}
